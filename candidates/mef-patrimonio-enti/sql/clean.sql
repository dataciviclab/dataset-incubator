SELECT
    TRIM("Settore Istituzionale") AS settore_istituzionale,
    TRIM("Macrocategoria Amministrazione") AS macrocategoria_amministrazione,
    TRIM("Tipologia Amministrazione") AS tipologia_amministrazione,
    TRIM("Amministrazione Denominazione") AS amministrazione_denominazione,
    TRIM(REPLACE(REPLACE("Amministrazione Codice Fiscale", '[', ''), ']', '')) AS amministrazione_codice_fiscale,
    TRIM("Regione (Amministrazione)") AS regione,
    TRIM("Provincia (Amministrazione)") AS provincia,
    TRIM("Comune (Amministrazione)") AS comune,
    TRIM("Cod. Comune (Amministrazione)") AS codice_comune,
    TRY_CAST("Numero beni in proprieta'" AS BIGINT) AS num_beni_proprieta,
    TRY_CAST("Numero beni in detenzione" AS BIGINT) AS num_beni_detenzione,
    CASE WHEN UPPER(TRIM("Dichiarazione negativa")) LIKE 'S%' THEN TRUE ELSE FALSE END AS dichiarazione_negativa,
    CASE WHEN UPPER(TRIM("Dich. di completezza dei dati")) LIKE 'S%' THEN TRUE ELSE FALSE END AS dichiarazione_completezza,
    CASE WHEN UPPER(TRIM("Invio comunicazione")) LIKE 'S%' THEN TRUE ELSE FALSE END AS invio_comunicazione,
    CASE WHEN UPPER(TRIM("Obbligo di comunicazione")) LIKE 'S%' THEN TRUE ELSE FALSE END AS obbligo_comunicazione,
    TRIM("Nome sezione") AS nome_sezione,
    TRIM("Nome file Beni Immobili Dichiarati") AS nome_file_immobili,
    TRIM("Nome file Detenzioni a favore di terzi") AS nome_file_detenzioni,
FROM raw_input
