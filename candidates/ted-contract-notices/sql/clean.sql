SELECT
    TRY_CAST(ID_NOTICE_CN AS BIGINT) AS id_notice_cn,
    TED_NOTICE_URL AS ted_notice_url,
    TRY_CAST(YEAR AS INTEGER) AS anno_pubblicazione,
    TRY_CAST(ID_TYPE AS INTEGER) AS id_tipo_notice,
    TRY_CAST(DT_DISPATCH AS DATE) AS data_invio,
    XSD_VERSION AS xsd_version,
    CANCELLED::BOOLEAN AS flag_cancellato,
    TRY_CAST(CORRECTIONS AS INTEGER) AS n_correzioni,
    TRY_CAST(FUTURE_CAN_ID AS BIGINT) AS future_can_id,
    FUTURE_CAN_ID_ESTIMATED::BOOLEAN AS flag_future_can_estimated,
    TRY_CAST(B_MULTIPLE_CAE AS BOOLEAN) AS flag_multipla_stazione,
    CAE_NAME AS stazione_appaltante_nome,
    CAE_NATIONALID AS stazione_appaltante_codice_nazionale,
    CAE_ADDRESS AS stazione_appaltante_indirizzo,
    CAE_TOWN AS stazione_appaltante_citta,
    CAE_POSTAL_CODE AS stazione_appaltante_cap,
    ISO_COUNTRY_CODE AS paese,
    TRY_CAST(B_MULTIPLE_COUNTRY AS BOOLEAN) AS flag_multi_paese,
    ISO_COUNTRY_CODE_ALL AS paesi_coinvolti,
    TRY_CAST(CAE_TYPE AS INTEGER) AS tipo_stazione_appaltante,
    EU_INST_CODE AS codice_istituzione_ue,
    MAIN_ACTIVITY AS attivita_principale,
    TRY_CAST(B_ON_BEHALF AS BOOLEAN) AS flag_per_conto_terzi,
    TRY_CAST(B_INVOLVES_JOINT_PROCUREMENT AS BOOLEAN) AS flag_appalto_congiunto,
    TRY_CAST(B_AWARDED_BY_CENTRAL_BODY AS BOOLEAN) AS flag_aggiudicato_centrale,
    TYPE_OF_CONTRACT AS tipo_contratto,
    TAL_LOCATION_NUTS AS nuts_luogo_esecuzione,
    TRY_CAST(B_FRA_AGREEMENT AS BOOLEAN) AS flag_accordo_quadro,
    TRY_CAST(FRA_ESTIMATED AS DOUBLE) AS accordo_quadro_stima,
    TRY_CAST(B_FRA_SINGLE_OPERATOR AS BOOLEAN) AS flag_operatore_unico,
    TRY_CAST(FRA_NUMBER_OPERATORS AS INTEGER) AS n_operatori_accordo,
    TRY_CAST(FRA_NUMBER_MAX_OPERATORS AS INTEGER) AS n_max_operatori_accordo,
    TRY_CAST(B_DYN_PURCH_SYST AS BOOLEAN) AS flag_sistema_dinamico,
    CPV AS cpv_codice,
    ID_LOT AS id_lotto,
    ADDITIONAL_CPVS AS cpv_codici_aggiuntivi,
    TRY_CAST(B_GPA AS BOOLEAN) AS flag_gpa,
    TRY_CAST(LOTS_NUMBER AS INTEGER) AS n_lotti,
    LOTS_SUBMISSION AS lotti_presentati,
    TRY_CAST(B_VARIANTS AS BOOLEAN) AS flag_varianti,
    TRY_CAST(REPLACE(VALUE_EURO, ',', '.') AS DOUBLE) AS valore_euro,
    TRY_CAST(REPLACE(VALUE_EURO_FIN_1, ',', '.') AS DOUBLE) AS valore_euro_fin_1,
    TRY_CAST(REPLACE(VALUE_EURO_FIN_2, ',', '.') AS DOUBLE) AS valore_euro_fin_2,
    TRY_CAST(B_OPTIONS AS BOOLEAN) AS flag_opzioni,
    TRY_CAST(B_EU_FUNDS AS BOOLEAN) AS flag_fondi_ue,
    TRY_CAST(B_RENEWALS AS BOOLEAN) AS flag_rinnovo,
    TRY_CAST(DURATION AS DOUBLE) AS durata_mesi,
    TRY_CAST(CONTRACT_START AS DATE) AS data_inizio_contratto,
    TRY_CAST(CONTRACT_COMPLETION AS DATE) AS data_fine_contratto,
    TOP_TYPE AS tipo_procedura,
    TRY_CAST(B_ACCELERATED AS BOOLEAN) AS flag_accelerata,
    TRY_CAST(ENV_OPERATORS AS INTEGER) AS n_operatori_inviati,
    TRY_CAST(ENV_MIN_OPERATORS AS INTEGER) AS n_min_operatori,
    TRY_CAST(ENV_MAX_OPERATORS AS INTEGER) AS n_max_operatori,
    CRIT_CODE AS codice_criterio_aggiudicazione,
    TRY_CAST(REPLACE(CRIT_PRICE_WEIGHT, ',', '.') AS DOUBLE) AS peso_prezzo,
    CRIT_CRITERIA AS criteri_qualitativi,
    CRIT_WEIGHTS AS pesi_criteri,
    TRY_CAST(B_ELECTRONIC_AUCTION AS BOOLEAN) AS flag_asta_elettronica,
    TRY_CAST(DT_APPLICATIONS AS DATE) AS data_scadenza_offerte,
    TRY_CAST(B_LANGUAGE_ANY_EC AS BOOLEAN) AS flag_lingua_qualsiasi_ce,
    ADMIN_LANGUAGES_TENDER AS lingue_ammesse,
    ADMIN_OTHER_LANGUAGES_TENDER AS altre_lingue_ammesse,
    TRY_CAST(B_RECURRENT_PROCUREMENT AS BOOLEAN) AS flag_appalto_ricorrente
FROM raw_input
WHERE ISO_COUNTRY_CODE = 'IT'
