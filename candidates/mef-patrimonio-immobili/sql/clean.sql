SELECT
    TRIM("Settore Istituzionale") AS settore_istituzionale,
    TRIM("Macrocategoria Amministrazione") AS macrocategoria_amministrazione,
    TRIM("Tipologia Amministrazione") AS tipologia_amministrazione,
    TRIM("Amministrazione Denominazione") AS amministrazione_denominazione,
    TRIM(REPLACE(REPLACE("Amministrazione Codice Fiscale", '[', ''), ']', '')) AS amministrazione_codice_fiscale,
    TRIM("Regione (Amministrazione)") AS reg_amministrazione,
    TRIM("Provincia (Amministrazione)") AS prov_amministrazione,
    TRIM("Comune (Amministrazione)") AS comune_amministrazione,
    TRIM("Cod. Comune (Amministrazione)") AS codice_comune_amministrazione,
    TRIM("Titolo proprietà") AS titolo_proprieta,
    TRY_CAST(REPLACE("Quota proprietà"::VARCHAR, ',', '.') AS DOUBLE) AS quota_proprieta,
    TRIM("Titolo detenzione") AS titolo_detenzione,
    TRIM("Detenzione Intera UI") AS detenzione_intera_ui,
    TRY_CAST(REPLACE("Superficie in detenzione"::VARCHAR, ',', '.') AS DOUBLE) AS superficie_detenzione_mq,
    TRY_CAST(REPLACE(REPLACE("Canone annuale"::VARCHAR, '.', ''), ',', '.') AS DOUBLE) AS canone_annuale,
    TRIM("Sogg. cedente (denominazione)") AS soggetto_cedente,
    TRIM("Soggetto cedente (CF)") AS soggetto_cedente_cf,
    TRY_CAST("ID bene" AS BIGINT) AS id_bene,
    TRIM("Natura del bene") AS natura_bene,
    TRIM("Stato Accatastamento") AS stato_accatastamento,
    TRIM("Sistema Catastale") AS sistema_catastale,
    TRIM("Regione del bene") AS regione_bene,
    TRIM("Provincia del bene") AS provincia_bene,
    TRIM("Comune del bene") AS comune_bene,
    TRIM("Codice Comune del bene") AS codice_comune_bene,
    TRIM("Indirizzo") AS indirizzo,
    TRIM("Numero Civico") AS numero_civico,
    TRIM("Fonte Georeferenziazione") AS fonte_georef,
    TRIM("Precisione Georeferenziazione") AS precisione_georef,
    TRIM("Immobile Geo-Ref.") AS immobile_georef,
    TRY_CAST(REPLACE("Latitudine"::VARCHAR, ',', '.') AS DOUBLE) AS latitudine,
    TRY_CAST(REPLACE("Longitudine"::VARCHAR, ',', '.') AS DOUBLE) AS longitudine,
    TRIM("Identificativo catastale") AS identificativo_catastale,
    TRIM("Codice Bene non accatastato") AS codice_bene_non_accatastato,
    TRIM("Tipologia Bene Immobile") AS tipologia_bene,
    TRY_CAST(REPLACE("Superficie (mq)"::VARCHAR, ',', '.') AS DOUBLE) AS superficie_mq,
    TRY_CAST(REPLACE("Cubatura (mc)"::VARCHAR, ',', '.') AS DOUBLE) AS cubatura_mc,
    TRY_CAST(REPLACE("Sup. aree pertinenziali (mq)"::VARCHAR, ',', '.') AS DOUBLE) AS sup_aree_pertinenziali_mq,
    TRY_CAST(REPLACE("Superficie di Riferimento (mq)"::VARCHAR, ',', '.') AS DOUBLE) AS superficie_riferimento_mq,
    TRIM("Epoca Costruzione") AS epoca_costruzione,
    TRIM("Vinc. culturale/paesaggistico") AS vincoli,
    TRIM("Natura Giuridica del Bene") AS natura_giuridica_bene,
    TRIM("Diritto gravante sul terreno") AS diritto_gravante_terreno,
    TRY_CAST("ID Compendio" AS BIGINT) AS id_compendio,
    TRIM("Descrizione Compendio") AS descrizione_compendio,
    TRIM("Tipologia Compendio") AS tipologia_compendio,
    TRIM("Utilizzo del bene") AS utilizzo_bene,
    TRIM("Finalità") AS finalita,
    TRIM("Altra finalità") AS altra_finalita,
    TRIM("ui data interamente a terzi") AS ui_data_interamente_terzi,
    TRIM("ui data parzialmente a terzi") AS ui_data_parzialmente_terzi,
FROM raw_input
