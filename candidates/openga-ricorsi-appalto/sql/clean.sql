SELECT
    TRY_CAST("ANNO_DEPOSITO_RICORSO" AS BIGINT) AS anno,
    TRY_CAST("CODICE_SEDE" AS BIGINT) AS codice_sede,
    trim("NOME_SEDE") AS nome_sede,
    TRY_CAST("CODICE_SEZIONE" AS BIGINT) AS codice_sezione,
    trim("NOME_SEZIONE") AS nome_sezione,
    TRY_CAST("NUMERO_RICORSO" AS BIGINT) AS numero_ricorso,
    TRY_CAST("DATA_DEPOSITO_RICORSO" AS DATE) AS data_deposito_ricorso,
    trim("CLASSIFICAZIONE_RICORSO") AS classificazione_ricorso,
    trim("CODICE_CIG") AS codice_cig,
    trim("CODICE_ACCORDO_QUADRO") AS codice_accordo_quadro,
    TRY_CAST("NUMERO_GARA" AS BIGINT) AS numero_gara,
    trim("OGGETTO_GARA") AS oggetto_gara,
    TRY_CAST(replace("IMPORTO_COMPLESSIVO_GARA", ',', '.') AS DOUBLE) AS importo_complessivo_gara,
    TRY_CAST("NUMERO_LOTTI_COMPONENTI" AS BIGINT) AS numero_lotti_componenti,
    trim("OGGETTO_LOTTO") AS oggetto_lotto,
    TRY_CAST(replace("IMPORTO_LOTTO", ',', '.') AS DOUBLE) AS importo_lotto,
    trim("STATO_GARA") AS stato_gara,
    trim("SETTORE") AS settore,
    trim("LUOGO_ISTAT") AS luogo_istat,
    trim("PROVINCIA") AS provincia,
    TRY_CAST("DATA_PUBBLICAZIONE" AS DATE) AS data_pubblicazione,
    TRY_CAST("DATA_SCADENZA_OFFERTA" AS DATE) AS data_scadenza_offerta,
    trim("CF_AMMINISTRAZIONE_APPALTANTE") AS cf_amministrazione_appaltante,
    trim("DENOMINAZIONE_AMMINISTRAZIONE_APPALTANTE") AS denominazione_amministrazione_appaltante
FROM raw_input
