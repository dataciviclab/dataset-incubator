SELECT
    TRY_CAST("ANNO_DEPOSITO_RICORSO" AS BIGINT) AS anno,
    TRY_CAST("CODICE_SEDE" AS BIGINT) AS codice_sede,
    TRY_CAST("NOME_SEDE" AS VARCHAR) AS nome_sede,
    TRY_CAST("CODICE_SEZIONE" AS BIGINT) AS codice_sezione,
    TRY_CAST("NOME_SEZIONE" AS VARCHAR) AS nome_sezione,
    TRY_CAST("NUMERO_RICORSO" AS BIGINT) AS numero_ricorso,
    TRY_CAST("DATA_DEPOSITO_RICORSO" AS DATE) AS data_deposito_ricorso,
    TRY_CAST("CLASSIFICAZIONE_RICORSO" AS VARCHAR) AS classificazione_ricorso,
    TRY_CAST("CODICE_CIG" AS VARCHAR) AS codice_cig,
    TRY_CAST("CODICE_ACCORDO_QUADRO" AS VARCHAR) AS codice_accordo_quadro,
    TRY_CAST("NUMERO_GARA" AS VARCHAR) AS numero_gara,
    TRY_CAST("OGGETTO_GARA" AS VARCHAR) AS oggetto_gara,
    TRY_CAST(replace("IMPORTO_COMPLESSIVO_GARA"::VARCHAR, ',', '.') AS DOUBLE) AS importo_complessivo_gara,
    TRY_CAST("NUMERO_LOTTI_COMPONENTI" AS BIGINT) AS numero_lotti_componenti,
    TRY_CAST("OGGETTO_LOTTO" AS VARCHAR) AS oggetto_lotto,
    TRY_CAST(replace("IMPORTO_LOTTO"::VARCHAR, ',', '.') AS DOUBLE) AS importo_lotto,
    TRY_CAST("STATO_GARA" AS VARCHAR) AS stato_gara,
    TRY_CAST("SETTORE" AS VARCHAR) AS settore,
    TRY_CAST("LUOGO_ISTAT" AS VARCHAR) AS luogo_istat,
    TRY_CAST("PROVINCIA" AS VARCHAR) AS provincia,
    TRY_CAST("DATA_PUBBLICAZIONE" AS DATE) AS data_pubblicazione,
    TRY_CAST("DATA_SCADENZA_OFFERTA" AS DATE) AS data_scadenza_offerta,
    TRY_CAST("CF_AMMINISTRAZIONE_APPALTANTE" AS VARCHAR) AS cf_amministrazione_appaltante,
    TRY_CAST("DENOMINAZIONE_AMMINISTRAZIONE_APPALTANTE" AS VARCHAR) AS denominazione_amministrazione_appaltante
FROM raw_input
